'use strict';

let dayNum = prompt ('Введите номер дня недели');
if (dayNum ==1) {
    alert('Понедельник');
    
}
else if (dayNum ==2) {
    alert('Вторник');
    
}
else if (dayNum ==3) {
    alert('Среда');
    
}
else if (dayNum ==4) {
    alert('Четверг');
    
}
else if (dayNum ==5) {
    alert('Пятница');
    
}
else if (dayNum ==6) {
    alert('Суббота');
    
}    
else if (dayNum ==7) {
    alert('Воскресенье');
        
} else {
    alert('Такого дня недели не существует!');
}